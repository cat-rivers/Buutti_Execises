//Area of a Rectangle
const width = 5;
const length = 8;
const area = width * length;
console.log(
  `area of a rectangle with length ${length} and width ${width} meters long is ${area} square meters`
);

//Area of a Triangle

const areaTriangle = (width * length) / 2;
console.log(
  `Area of a triangle with cathetuses ${width} and ${length} is: ${areaTriangle}`
);
